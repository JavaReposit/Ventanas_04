    private void initHelp(){
        File fichero = new File(".\\src\\help\\help_set.hs");
        try {
            URL hsURL = fichero.toURI().toURL();
            // Crea el HelpSet y el HelpBroker
            HelpSet helpSet = new HelpSet(null, hsURL);
            HelpBroker hb = helpSet.createHelpBroker();
            hb.enableHelpKey(getRootPane(),"aplicacion", helpSet);
            hb.enableHelpOnButton(jMenuItemAyuda, "aplicacion", helpSet);
        } catch (Exception e) {
            System.err.println("No se encuentra el fichero help");  
        }
    }
    
    private void refrescarTabla(){
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String [] {"Codigo","CIF","Razon Social"});
        List<Empresa> listaEmpresas = ListaEmpresas.getListaEmpresas();
        for (Empresa empresa: listaEmpresas){
            dtm.addRow(new String[]{empresa.codigo,empresa.CIF,empresa.razonSocial});
        }
        jTableEmpresas.setModel(dtm);
    }

